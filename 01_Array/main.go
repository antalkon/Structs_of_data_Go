package main

import "fmt"

func main() {
	// Объявляем массив целых чисел длиной 5
	var numbers [5]int

	// Инициализируем массив значениями
	numbers[0] = 10
	numbers[1] = 20
	numbers[2] = 30
	numbers[3] = 40
	numbers[4] = 50

	// Выводим элементы массива
	fmt.Println("Элементы массива:", numbers)

	// Длина массива
	fmt.Println("Длина массива:", len(numbers))

	// Перебираем элементы массива с помощью цикла
	fmt.Println("Элементы массива с помощью цикла:")
	for i := 0; i < len(numbers); i++ {
		fmt.Printf("Элемент %d: %d\n", i, numbers[i])
	}

	// Пример изменения элемента массива
	numbers[2] = 100
	fmt.Println("После изменения 3-го элемента:", numbers)

	// Массив можно инициализировать сразу с помощью литерала
	fruits := [3]string{"яблоко", "банан", "апельсин"}
	fmt.Println("Фрукты:", fruits)

	// Массивы в Go имеют фиксированную длину, поэтому вы не можете изменить длину после создания.
}
